package("endstone")
   add_urls("https://raw.githubusercontent.com/YoumiHa/endstone/main/endstone.tar")
   add_versions("latest","0def1b513cf496d6195008e47dc26e6363900df2cdb5b193157dffce4806e32c")
    on_install("windows", function (package)
        os.iorun("conan profile detect --force")
        os.iorun("conan export third_party/funchook --version 1.1.3")
        os.iorun("conan build . --build=missing -s compiler.cppstd=17 -s build_type=Debug -c tools.cmake.cmaketoolchain:generator=Ninja -build-folder=build")
        os.iorun("mkdir lib")
        os.iorun("mkdir lib\include")
        os.iorun("xcopy build\Debug\endstone_runtime.dll lib")
        os.iorun("xcopy build\Debug\endstone_runtime.lib lib ")
        os.iorun("xcopy /E include\endstone lib\include")
        os.iorun("xcopy /E include\bedrock lib\include ")
        os.cp("lib/include/bedrock", package:installdir("include"))
        os.cp("lib/include/endstone", package:installdir("include"))
        os.cp("lib/endstone_runtime.lib", package:installdir("lib"))
    end)
